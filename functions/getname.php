<?php

function getCollectionName($collection) {
	if ($collection == "g-shock") {
		return "G-SHOCK";
	}
	if ($collection == "g-shock-premium") {
		return "G-SHOCK PREMIUM";
	}
	if ($collection == "baby-g") {
		return "Baby-G";
	}
	if ($collection == "casio-sport") {
		return "Casio Sport";
	}
	if ($collection == "edifice") {
		return "EDIFICE";
	}
	if ($collection == "sheen") {
		return "SHEEN";
	}
	if ($collection == "wave-ceptor") {
		return "WAVE CEPTOR";
	}
	if ($collection == "casio-collection") {
		return "Casio Collection";
	}
	if ($collection == "clocks") {
		return "Clocks";
	}
}